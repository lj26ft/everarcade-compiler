#!/usr/bin/env bash
set -euo pipefail
ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"
export CARGO_BUILD_JOBS="${CARGO_BUILD_JOBS:-1}"
CARGO_FLAGS=()
for arg in "$@"; do
  case "$arg" in
    --offline|--locked|--frozen) CARGO_FLAGS+=("$arg") ;;
    *) echo "unknown argument: $arg" >&2; exit 2 ;;
  esac
done
REPORT="deployment/reports/anchor_validation_run.md"
test -f xrpl/publisher/README.md
cargo test -p execution-core --test evernode_deployment_tests "${CARGO_FLAGS[@]}"
cat > "$REPORT" <<'REPORT_EOF'
# Anchor Validation Run

status: passed
ReceiptAnchorRecord: generated
ReplayAnchorRecord: generated
WorldAnchorRecord: generated
DeploymentAnchorRecord: generated
publication payload: verified
anchor replay: verified
REPORT_EOF
echo "anchor validation passed; report=$REPORT"

bash scripts/generate_evernode_packages.sh
bash scripts/run_xrpl_anchor_validation.sh "$@"

mkdir -p deployment/reports
cat > deployment/reports/xrpl_launch_gate.md <<'RPT'
# XRPL Launch Gate

| Domain | Classification | Evidence |
| --- | --- | --- |
| intent generation | Ready | Runtime records include receipt, replay, world, and deployment intent data. |
| anchor generation | Ready | Anchor records are generated deterministically. |
| publication | Partially Ready | Publication payloads are generated and verified, but actual XRPL submission remains external. |
| verification | Ready | Payload and replayed anchor equivalence are validated. |

- deployment artifacts: generated by `scripts/generate_evernode_packages.sh`
- source control policy: generated `.tar.gz` and `.sig` files are not source controlled
- XRPL runtime submission boundary: external operator only
- anchor validation: validated
RPT

echo "anchor validation complete"
